package com.ldtsfeup2526.bobTheDestructor.gui;

public class Resolution {
    int width;
    int height;

    public Resolution(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
